
package psp;


public class Posiciones {
    
    private int posx;
    private int posy;

    public Posiciones(int posx, int posy) {
        this.posx = posx;
        this.posy = posy;
    }

    public void setPosx(int posx) {
        this.posx = posx;
    }

    public void setPosy(int posy) {
        this.posy = posy;
    }

    public int getPosx() {
        return posx;
    }

    public int getPosy() {
        return posy;
    }

}
